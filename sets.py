# Write a Python program to find the intersection of two sets.
set1 = {12,32,40,45,22,73,31}
set2 = {12,31,55,60,23,32,22}
intersection_set = set1.intersection(set2)
print("Intersection of both sets is : ",intersection_set)

# Write a Python program to find the union of two sets.
set1 = {10,23,42,22,33}
set2 = {22,38,53,92,21}
union_set = set1.union(set2)
print("Union of both sets is : ",union_set)